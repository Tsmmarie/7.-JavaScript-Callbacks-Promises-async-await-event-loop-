function mySetTimeout(delay) {
    return new Promise((resolve) => {
        setTimeout(resolve, delay);
    });
}

function makeToy(productionTime) {
    return mySetTimeout(productionTime).then(() => {
        console.log("рЃАрЃљрЃЌрЃљрЃЏрЃљрЃерЃЮ рЃЊрЃљрЃЏрЃќрЃљрЃЊрЃЊрЃљ!");
        return "Toy";
    });
}

function deliverToys(deliveryTime) {
    return mySetTimeout(deliveryTime).then(() => {
        console.log("рЃАрЃљрЃЌрЃљрЃЏрЃљрЃерЃЮ рЃЏрЃўрЃћрЃгрЃЮрЃЊрЃљ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃА!");
    });
}

